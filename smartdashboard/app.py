def format_inr_text(value):
    if value >= 10000000:
        return f"₹{value/10000000:.2f} Crores"
    elif value >= 100000:
        return f"₹{value/100000:.2f} Lakhs"
    else:
        # standard fallback
        s = str(int(value))
        if len(s) <= 3:
            return f"₹{s}"
        
        last_three = s[-3:]
        remaining = s[:-3]
        chunks = []
        while len(remaining) > 0:
            chunks.insert(0, remaining[-2:])
            remaining = remaining[:-2]
        formatted = ",".join(chunks) + "," + last_three
        formatted = ",".join(chunks) + "," + last_three
        return f"₹{formatted}"

