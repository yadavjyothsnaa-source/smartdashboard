@app.route('/api/sales-trend', methods=['GET'])
def get_sales_trend():
    try:
        conn = sqlite3.connect('../business_analytics.db')
        conn.row_factory = sqlite3.Row
        cursor = conn.cursor()
        
        # Group by Month/Year and SUM Revenue and Profit
        # We assume Date is formatted 'YYYY-MM-DD' or similar
        query = """
        SELECT strftime('%m', Date) as month_num,
               SUM(Revenue) as revenue,
               SUM(Profit) as profit
        FROM Business_Data
        WHERE Date IS NOT NULL
        GROUP BY month_num
        ORDER BY month_num ASC
        """
        cursor.execute(query)
        rows = cursor.fetchall()
        
        month_map = {'01':'Jan', '02':'Feb', '03':'Mar', '04':'Apr', 
                     '05':'May', '06':'Jun', '07':'Jul', '08':'Aug',
                     '09':'Sep', '10':'Oct', '11':'Nov', '12':'Dec'}
                     
        data = []
        for r in rows:
            if r['month_num'] in month_map:
                data.append({
                    "month": month_map[r['month_num']],
                    "revenue": round(r['revenue'] or 0, 2),
                    "profit": round(r['profit'] or 0, 2)
                })
                
        return jsonify(data)
    except Exception as e:
        print(f"Error in sales-trend: {e}")
        return jsonify([])
    finally:
        if 'conn' in locals():
            conn.close()

