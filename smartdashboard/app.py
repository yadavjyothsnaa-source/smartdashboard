@app.route('/api/upload', methods=['POST'])
def upload_file():
    try:
        if 'csv' not in request.files:
            return jsonify({"error": "No file uploaded. Please select a CSV file."}), 400
            
        file = request.files['csv']
        if not file.filename.endswith('.csv'):
            return jsonify({"error": "Invalid file type. Please upload a valid .csv file"}), 400

        sender_role = request.form.get('role', 'analyst')
        user_id = 1 if sender_role == 'analyst' else 2 # Mocking DB ID
        
        # Save file format
        uploads_dir = os.path.join('..', 'public', 'uploads')
        os.makedirs(uploads_dir, exist_ok=True)
        filename = f"{int(time.time())}_{file.filename}"
        filepath = os.path.join(uploads_dir, filename)
        file.save(filepath)

        # 1. READ CSV (Pandas)
        df = pd.read_csv(filepath)

        # 2. TRAIN MODEL DYNAMICALLY
        try:
            model, accuracy, top_features = train_model(df, target_col="Revenue")
        except Exception as ml_err:
            return jsonify({"error": f"ML Training Error: Ensure dataset has 'Revenue' target column. ({str(ml_err)})"}), 400

        # 3. PREDICT (Using last row as sample future)
        # Drop the target to use the rest of the dataframe structure
        sample_df = df.copy()
        if 'Date' in sample_df.columns:
            sample_df['Date'] = pd.to_datetime(sample_df['Date'])
            sample_df['Month'] = sample_df['Date'].dt.month
            sample_df['Year'] = sample_df['Date'].dt.year
            sample_df = sample_df.drop('Date', axis=1)
            
        sample_df = sample_df.dropna(subset=['Revenue'])
        sample_df = pd.get_dummies(sample_df, drop_first=True)
        sample_x = sample_df.drop("Revenue", axis=1).iloc[-1:]
        
        predicted_val = predict(model, sample_x)[0]
        
        # Determine trend vs last actual row
        last_actual = df['Revenue'].dropna().iloc[-1]
        trend = "upward" if predicted_val > last_actual else "downward"

        # 4. DATABASE INSERTIONS
        conn = sqlite3.connect('../business_analytics.db')
        cursor = conn.cursor()
        
        cursor.execute("INSERT INTO uploaded_data (user_id, file_name, file_path) VALUES (?, ?, ?)", 
                      (user_id, filename, f"/uploads/{filename}"))
        upload_id = cursor.lastrowid
        
        result_json = {
            "trend": trend,
            "confidence": f"{accuracy * 100:.1f}%",
            "forecast_next_period": f"₹{predicted_val:,.2f}",
            "top_features": top_features,
            "model_used": "RandomForestRegressor"
        }
        
        cursor.execute("INSERT INTO ml_predictions (user_id, upload_id, result) VALUES (?, ?, ?)",
                      (user_id, upload_id, json.dumps(result_json)))
                      
        # Notifications
        cursor.execute("INSERT INTO notifications (user_id, message) VALUES (?, ?)", 
                      (user_id, f"📊 ML Prediction complete! Trend: {trend}, Accuracy: {accuracy*100:.1f}%"))
        
        cursor.execute("SELECT id FROM users WHERE role = 'admin' LIMIT 1")
        admin = cursor.fetchone()
        if admin:
            cursor.execute("INSERT INTO notifications (user_id, message) VALUES (?, ?)", 
                          (admin[0], f"📥 New data uploaded: {file.filename} (Trained dynamically)"))
                          
        conn.commit()
        
        return jsonify({
            "success": True,
            "message": "ML processed successfully",
            "prediction": f"₹{predicted_val:,.2f}",
            "accuracy": round(accuracy, 4),
            "top_features": top_features,
            "trend": trend
        })

    except Exception as e:
        print(f"Upload error: {e}")
        return jsonify({"error": str(e)}), 500
    finally:
        if 'conn' in locals():
            conn.close()

