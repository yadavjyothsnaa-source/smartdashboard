def check_alerts():
    try:
        conn = sqlite3.connect('../business_analytics.db')
        cursor = conn.cursor()
        alerts = []
        
        # Condition 1: High sales spike
        cursor.execute("SELECT Product, SUM(Units_Sold) FROM Business_Data GROUP BY Product ORDER BY SUM(Units_Sold) DESC LIMIT 1")
        top_sales = cursor.fetchone()
        if top_sales and top_sales[1] > 1000:
            alerts.append({"id": 1, "message": f"High sales spike detected for {top_sales[0]} ({top_sales[1]} units)!", "type": "critical"})
            
        # Condition 2: Revenue drop (we use Profit_Status or Loss as a proxy)
        cursor.execute("SELECT Product, SUM(Loss) FROM Business_Data WHERE Loss > 0 GROUP BY Product ORDER BY SUM(Loss) DESC LIMIT 1")
        high_loss = cursor.fetchone()
        if high_loss and high_loss[1] > 10000:
            alerts.append({"id": 2, "message": f"Critical revenue loss detected in {high_loss[0]} ({format_inr_text(high_loss[1])} lost).", "type": "critical"})
            
        # Standard notification
        cursor.execute("SELECT COUNT(*) FROM Business_Data")
        count = cursor.fetchone()[0]
        alerts.append({"id": 3, "message": f"System active tracking {count} records.", "type": "info"})
        
        return alerts
    except Exception as e:
        print(f"Alerts error: {e}")
        return []
    finally:
        if 'conn' in locals():
            conn.close()

@app.route('/api/notifications', methods=['GET'])
def get_notifications():
    alerts = check_alerts()
    return jsonify(alerts)

