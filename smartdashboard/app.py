@app.route('/api/revenue-by-region', methods=['GET'])
def get_revenue_by_region():
    try:
        conn = sqlite3.connect('../business_analytics.db')
        conn.row_factory = sqlite3.Row
        cursor = conn.cursor()
        
        # 1. Get exact total revenue per region
        cursor.execute("""
        SELECT Region as region, SUM(Revenue) as revenue
        FROM Business_Data
        GROUP BY Region
        ORDER BY revenue DESC
        """)
        region_rows = cursor.fetchall()
        
        # 2. Get breakdown subData per Product per Region
        cursor.execute("""
        SELECT Region, Product, SUM(Revenue) as rev
        FROM Business_Data
        GROUP BY Region, Product
        """)
        breakdown_rows = cursor.fetchall()
        
        # Build nested object
        results = []
        for r in region_rows:
            region_name = r['region']
            sub_data = []
            for b in breakdown_rows:
                if b['Region'] == region_name:
                    sub_data.append({
                        "name": b['Product'],
                        "rev": round(b['rev'] or 0, 2)
                    })
            results.append({
                "region": region_name,
                "revenue": round(r['revenue'] or 0, 2),
                "subData": sorted(sub_data, key=lambda x: x['rev'], reverse=True)
            })
            
        return jsonify(results)
    except Exception as e:
        return jsonify([])
    finally:
        if 'conn' in locals():
            conn.close()

